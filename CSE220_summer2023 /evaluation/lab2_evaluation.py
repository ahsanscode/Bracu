def moving_around(wrld,cmds):
    wrld[3][3] = '@'
    row,col = 3,3

    for var in range(len(cmds)):
      cmd = cmds[var]
      if cmd == 'U':
        row-=1
        wrld[row][col] = '*'
      if cmd == 'D':
        row+=1
        wrld[row][col] = '*'
      if cmd == 'L':
        col-=1
        wrld[row][col] = '*'
      if cmd == 'R':
        col+=1
        wrld[row][col] = '*'



    wrld[row][col] = '#'
    return wrld


